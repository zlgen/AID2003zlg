"""
    算数运算符
        +  -  *  幂运算**  /   //   %
    练习:exercise06/07
"""

data01 = 5
data02 = 2
print(data01 + data02)  # 算数运算 3
print(data01 ** data02)  # 5 ** 2  -->  5的二次方
print(data01 // data02)  # 2  除法运算,获取整数商
print(data01 % data02)  #  1  除法运算,获取余数
print(data01 / data02)  #  2.5 除法运算,获取小数商

print("5" + "2")  # 字符拼接 "52"

